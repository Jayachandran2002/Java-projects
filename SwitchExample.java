package exercisePackage;

public class SwitchExample {
	
	public static void main(String[] args) {
		int a = 20;
		switch (a) {
		case 20: 
			System.out.println("The Number is 20");
			break;
		
		case 10:
			System.out.println("The Number is 10");
			break;
		
		default:
			System.out.println("No Number Found");
		}
	}
}
